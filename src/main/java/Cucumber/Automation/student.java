package Cucumber.Automation;


public class student {
		private String name;
		private int rollnumber;
		
		public student(String name, int rollnumber)
		{
			this.name =name;
			this.rollnumber=rollnumber; 
		}
		public String getname() {
		return name;	
		}
		public int getrollnumber() {
			return rollnumber;
		}

}
